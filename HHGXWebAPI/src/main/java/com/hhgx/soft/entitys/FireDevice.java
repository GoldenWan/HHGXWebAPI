package com.hhgx.soft.entitys;

public class FireDevice {
	private String deviceNo     ;
    private String devicename   ;
    private String manufacture  ;
    private String model        ;
    private String productDate  ;
    private String validate     ;
    private String setupDate    ;
    private String setLocation  ;
    private String memo         ;
    private String siteid       ;
    private String iDeviceType  ;
	public String getDeviceNo() {
		return deviceNo;
	}
	public void setDeviceNo(String deviceNo) {
		this.deviceNo = deviceNo;
	}
	public String getDevicename() {
		return devicename;
	}
	public void setDevicename(String devicename) {
		this.devicename = devicename;
	}
	public String getManufacture() {
		return manufacture;
	}
	public void setManufacture(String manufacture) {
		this.manufacture = manufacture;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getProductDate() {
		return productDate;
	}
	public void setProductDate(String productDate) {
		this.productDate = productDate;
	}
	public String getValidate() {
		return validate;
	}
	public void setValidate(String validate) {
		this.validate = validate;
	}
	public String getSetupDate() {
		return setupDate;
	}
	public void setSetupDate(String setupDate) {
		this.setupDate = setupDate;
	}
	public String getSetLocation() {
		return setLocation;
	}
	public void setSetLocation(String setLocation) {
		this.setLocation = setLocation;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getSiteid() {
		return siteid;
	}
	public void setSiteid(String siteid) {
		this.siteid = siteid;
	}
	public String getiDeviceType() {
		return iDeviceType;
	}
	public void setiDeviceType(String iDeviceType) {
		this.iDeviceType = iDeviceType;
	} 
    
}
